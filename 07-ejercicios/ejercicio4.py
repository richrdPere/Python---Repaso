"""
Ejercicio 4.
    Ingrese dos numeros al usuario y hacer todas las operaciones basicas de una calculadora y mostrarlo en pantalla.
"""

nro1 = int(input("Ingrese el primer numero: "))
nro2 = int(input("Ingrese el segundo numero: "))

# Suma
print(f"La suma de sus numeros es {nro1+nro2}")

# Resta
print(f"La resta de sus numeros es {nro1-nro2}")

# Multiplicacion
print(f"El producto de sus numeros es {nro1*nro2}")

# Division
print(f"La division de sus numeros es {nro1/nro2}")